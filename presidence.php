    <p><br> Durant son premier septennat, Chirac a instauré, entre autre, le quiquenat présidentielle, dont il profitera en 2002, lors de sa réelection.<br></p>
        <h5 class="titrepetit"><br> Lutte contre le cancer </h5>
    <p><br>Le 14 juillet 2002, lors du traditionnel discours de la Fête nationale,
     Jacques Chirac fait de la lutte contre le cancer une priorité nationale.
      Un discours suivi d’effets, jusqu’à l’entrée en vigueur d’une loi phare,
       le 1er janvier 2008 : après des semaines de débats, c’est sous sa présidence qu’entre en vigueur l’interdiction de fumer dans les lieux publics fermés,
        comme les restaurants, les cafés, ou les boîtes de nuit. Une mesure qui fait grincer des dents au début, mais qui, mois après mois,
         entre dans les habitudes des Français.<p>

<p><br>Cette loi, c’est la mesure phare du président Chirac, et de son plan de santé publique pour lutter contre le cancer.
 Un plan, qui a fait des émules puisqu’on en est aujourd’hui au troisième plan contre le cancer.<br></p>
        <h5 class="titrepetit"><br> Fin du service militaire obligatoire </h5>
 <p><br>Autre décision de Jacques Chirac qui marque la vie des Français, des jeunes en particulier,
      sa décision de professionnaliser l’armée française et au passage de suspendre le service militaire obligatoire,
       remplacé par une Journée d’appel et de Préparation à la Défense, désormais appelée Journée Défense et Citoyenneté.<br>

Lors de son deuxième mandat,
 Jacques Chirac s’attaque à la sécurité routière, entre 2003 et 2007,
  et parvient à faire diminuer de 40% le nombre de morts sur les routes. Autre loi phare de son second mandat,
   la loi de février 2005, en faveur des personnes handicapées. Elle se traduit par la création d’une prestation compensatoire au handicap.<br></p>

