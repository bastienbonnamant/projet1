    <h3 class="about1">A propos</h3>
    <div class="about3"> 

        <img src="images/chichi.jpg" alt="chiraaac" class="chiraac">

        <p class="about">
            Les Guignols de l'info est une émission de télévision satirique française de marionnettes, diffusée entre le 29 août 1988 et le 22 juin 2018 sur Canal+.

            Parodie de journal télévisé, l’émission est une caricature du monde politique, des médias, des personnalités ou plus généralement de la société française et du monde actuel.

            Initialement créée pour pallier l'arrêt du JTN des Nuls, l'émission résulte de l'idée d'Alain de Greef qui désirait un équivalent français de Spitting Image. Arnold Boiseau s'est ensuite occupé de la mise en place de l'équipe. L'émission voit le jour initialement sous le titre Les Arènes de l’info, puis est renommée en 1990 Les Guignols de l'info avant de prendre le titre Les Guignols en 2015; l'émission étant alors moins axée sur la politique française.

            Lancée dès le 29 août 1988, les Arènes de l'info seront en concurrence frontale avec Le Bébête show de TF1 (séquence créée en 1982 dans Co-Co Boy), devenue une émission quotidienne dès le 14 mars 19881,2 à 19 h 50. Les « Bébêtes » réalisant en moyenne 30 % d'audience3. Après des débuts difficiles, l'émission subit une restructuration et atteint une réelle notoriété en 1991 comme l'ensemble de Nulle part ailleurs à laquelle elle est intégrée.

            Les Guignols de l'info ont un poids important sur la culture populaire française : la répétition des phrases fétiches et des stéréotypes des personnages a permis de rendre parfois plus populaire la marionnette que la personne elle-même. L'émission a parfois contribué à forger l'image de certaines personnalités, notamment politiques, dans la culture populaire : l'exemple le plus éclatant en est sans doute Jacques Chirac4.

            Son influence décline cependant à la fin des années 19905. Néanmoins, l'audience reste très élevée, de loin la plus haute de Canal+ : 3 millions de téléspectateurs en 19976 ou en 20037, 2,8 millions en 20078. À partir de 2010, l'audience décline de façon significative (1,8 million en 2015), mais reste toujours la plus élevée de la chaîne.

            Mais c'est surtout la reprise en main éditoriale de l'émission par Vincent Bolloré en 2015 qui fait sombrer l'émission. Après plusieurs versions, divers changements de cases horaires, le remplacement de tous les coauteurs, une ligne éditoriale moins axée sur la satire politique, et une baisse conséquente de son budget9, l'émission s'arrête en 2018 dans une certaine indifférence10 (environ 150 000 téléspectateurs chaque soir11).
        </p>

