    <h3 class="about4">Les marionettes</h3>
    <div class="explications">

        <p class="init">
            Initialement, une vingtaine de marionnettes avaient été fabriquées pour l'émission qui en compte aujourd'hui plus de 30021. En moyenne, 15 marionnettes sont utilisées pour chaque émission, même si cela peut dépasser la centaine pour tourner les fictions16. Une trentaine de marionnettistes travaillent en intermittence pour l'émission18. Les marionnettes sont réalisées à la demande des auteurs : Alain Duverne contacte alors Bruno Marty pour l'informer des futures personnalités à caricaturer22.
            La conception et la réparation des marionnettes est assurée par quatre permanents, sous l'égide d'Alain Duverne23. La fabrication d'une marionnette prend environ un mois et est coûteuse : la matrice coûte 6 000 € et le tirage 2 500 €. 
        </p>

